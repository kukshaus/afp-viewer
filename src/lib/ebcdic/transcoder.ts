// Reverse map (Unicode code-point → EBCDIC byte) — built lazily on first use.
let unicodeToCP037: Map<number, number> | null = null;

function getReverse037(): Map<number, number> {
  if (!unicodeToCP037) {
    unicodeToCP037 = new Map();
    for (let i = 0; i < 256; i++) {
      unicodeToCP037.set(CP037_TO_UNICODE[i], i);
    }
  }
  return unicodeToCP037;
}

