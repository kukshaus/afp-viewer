// CP500 key differences from CP037 for German/European characters:
// 0x4A = [ (CP037: ¢)     0x4F = ! (CP037: |)
// 0x5A = ] (CP037: !)     0x5F = ^ (CP037: ¬)
// 0xA1 = ß (CP037: ~)     0xB0 = ¢ (CP037: °)
// 0xBA = ¬ (CP037: [)     0xBB = | (CP037: ])
// 0xC0 = ä (CP037: {)     0xD0 = ü (CP037: })
// 0xDC = Ü (CP037: Ü same) 0xE0 = Ö (CP037: \)
// 0xEC = Ä (CP037: Ö)     0xFC = Æ (CP037: Ü)

// ---------------------------------------------------------------------------
// Custom code-page registry
// ---------------------------------------------------------------------------

const customPages = new Map<string, number[]>();

/**
 * Register a custom EBCDIC code-page mapping.
 * @param name  A short identifier such as "cp500" or "cp1047".
 * @param table An array of 256 Unicode code-points indexed by EBCDIC byte.
 */
export function registerCodePage(name: string, table: number[]): void {
  if (table.length !== 256) {
    throw new RangeError(`Code-page table must have exactly 256 entries (got ${table.length})`);
  }
  customPages.set(name, table);
}

// ---------------------------------------------------------------------------
// Public API
// ---------------------------------------------------------------------------

let _defaultCodePage = 'cp500';

/**
 * Sets the default code page for EBCDIC decoding.
 * Called by the page parser when it detects the document's code page.
 */
export function setDefaultCodePage(codePage: string): void {
  _defaultCodePage = codePage;
}

/**
 * Decode EBCDIC bytes to a Unicode string.
 *
 * @param bytes     Raw EBCDIC-encoded bytes.
 * @param codePage  Optional code-page name. Uses auto-detected default if not specified.
 * @returns Decoded Unicode string.
 */
export function ebcdicToUnicode(bytes: Uint8Array, codePage?: string): string {
  const cp = codePage ?? _defaultCodePage;
  let table: number[] | undefined;
  if (cp === 'cp037') table = CP037_TO_UNICODE;
  else if (cp === 'cp500') table = CP500_TO_UNICODE;
  else if (cp === 'cp297' || cp === 'cp1147' || cp === 'cp1144') table = CP297_TO_UNICODE;
  else table = customPages.get(cp);

  if (!table) {
    throw new Error(`Unknown EBCDIC code page: ${codePage}`);
  }

  const chars: string[] = new Array(bytes.length);
  for (let i = 0; i < bytes.length; i++) {
    chars[i] = String.fromCharCode(table[bytes[i]]);
  }
  return chars.join('');
}

/**
 * Encode a Unicode string to EBCDIC bytes.
 *
 * @param text      Unicode string to encode.
 * @param codePage  Optional code-page name. Defaults to "cp037".
 * @returns EBCDIC-encoded byte array.
 */
export function unicodeToEbcdic(text: string, codePage = 'cp037'): Uint8Array {
  if (codePage !== 'cp037') {
    const table = customPages.get(codePage);
    if (!table) throw new Error(`Unknown EBCDIC code page: ${codePage}`);
    const reverse = new Map<number, number>();
    for (let i = 0; i < 256; i++) reverse.set(table[i], i);
    const out = new Uint8Array(text.length);
    for (let i = 0; i < text.length; i++) {
      out[i] = reverse.get(text.charCodeAt(i)) ?? 0x3F; // 0x3F = EBCDIC '?'
    }
    return out;
  }

  const reverse = getReverse037();
  const out = new Uint8Array(text.length);
  for (let i = 0; i < text.length; i++) {
    out[i] = reverse.get(text.charCodeAt(i)) ?? 0x3F;
  }
  return out;
}

/**
 * Decode a single EBCDIC byte to its Unicode character.
 */
export function ebcdicByteToChar(byte: number, codePage = 'cp037'): string {
  const table = codePage === 'cp037'
    ? CP037_TO_UNICODE
    : customPages.get(codePage);
  if (!table) throw new Error(`Unknown EBCDIC code page: ${codePage}`);
  return String.fromCharCode(table[byte & 0xFF]);
}
